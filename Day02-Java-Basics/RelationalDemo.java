public class RelationalDemo {
    public static void main(String[]args)
    {
        int a,b,c;
        a=1;
        b=2;
        c=1;
        System.out.println("a<b:"+(a<b));
        System.out.println("a>b:"+(a>b));
        System.out.println("a<=c:"+(a<=c));
        System.out.println("a>=b:"+(a>=b));
        System.out.println("a!=b:"+(a!=b));
        System.out.println("a==C:"+(a==c));
    }
}
